async def generate_post_content(
    topic: str,
    scenario: str = None,
    *,
    tone_mode: str = "soft",
    situation_category: str | None = None,
    situation_brief: str | None = None,
    recent_final_hooks: list[str] | None = None,
) -> dict:
    """
    Обращается к нейросетям в OpenRouter с поддержкой Fallback и Retries.
    """
    if not scenario or scenario not in SCENARIO_TEMPLATES:
        scenario = random.choice(list(SCENARIO_TEMPLATES.keys()))
        
    logger.info(f"🏭 ГЕНЕРАЦИЯ ТЕКСТА. Сценарий: '{scenario}'. Тема: '{topic}'")
    
    url = "https://openrouter.ai/api/v1/chat/completions"
    headers = {
        "Authorization": f"Bearer {settings.OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://foodflow.app",
        "X-Title": "FoodFlow Content Factory"
    }
    
    # Динамический контекст времени
    now = datetime.datetime.now()
    months = ['январь', 'февраль', 'март', 'апрель', 'май', 'июнь', 'июль', 'август', 'сентябрь', 'октябрь', 'ноябрь', 'декабрь']
    current_month = months[now.month - 1]
    
    if now.month in [12, 1, 2]: season = "Зима"
    elif now.month in [3, 4, 5]: season = "Весна"
    elif now.month in [6, 7, 8]: season = "Лето"
    else: season = "Осень"
    
    scenario_instruction = SCENARIO_TEMPLATES[scenario]
    
    tone_mode = (tone_mode or "soft").lower()
    if tone_mode not in {"soft", "hard"}:
        tone_mode = "soft"

    tone_rules = (
        "SOFT TONE:\n"
        "- supportive, friendly, without sarcasm\n"
        "- no shame, no labels, no ridicule\n"
        "- motivate through clarity and ease\n"
        if tone_mode == "soft"
        else
        "HARD TONE (ALLOWED TO BE TOUGH, NOT RUDE):\n"
        "- short, punchy, assertive\n"
        "- no insults, no shame, no labels (no \"нормальный человек\", no \"тупо\", no \"цирк\")\n"
        "- pressure through contrast (slow/manual vs fast/simple), not through humiliation\n"
    )

    banned_cliches = [
        "через неделю с foodflow",
        "разница — одно фото",
        "либо считаешь, либо забиваешь",
        "без ручного ввода",
        "кбжу за 10 секунд",
        "кбжу за секунды",
        "держать под контролем",
    ]

    banned_soft_endings = [
        "остальное",
        "не требует лишних переживаний",
        "бот возьмёт на себя",
        "вопрос регулярности",
        "главное — регулярность",
    ]

    lecture_phrases = [
        "смысл простой",
        "худеют не за счёт",
        "важно понимать",
        "дело в том что",
        "на самом деле",
    ]

    recent_final_hooks = list(recent_final_hooks or [])[-2:]

    prompt = f"""
ЦЕЛЕВАЯ АУДИТОРИЯ (ЦА):
- РФ, обычные люди (не айтишники), которым важно понимать что они едят: фитнес/похудение/просто контроль.
- Пиши простыми словами, без техно-жаргона и “внутренней кухни”.

ЗАПРЕЩЕНО (пример и почему):
- Плохо: «считать КБЖУ руками — это как писать код в блокноте» — это IT-метафора, отталкивает не-айтишную ЦА и звучит как снобизм.
- Делай вместо этого бытовые сравнения или вообще без метафор.

Твоя роль: Циничный, но заботливый "Кибер-Диетолог" проекта FoodFlow. Ты общаешься напрямую с аудиторией в Telegram-канале.

ВАЖНО:
- НИКОГДА не упоминай внутренние названия из кода/функций/модулей/классов (например, "UniversalInput", "AIBrainService" и т.п.).
- Пиши как для обычных людей: "просто отправь фото", "скинь голосом", "отправь чек в бот".
- НЕ ИСПОЛЬЗУЙ шейминг/унижение/ярлыки: нельзя \"нормальный человек\", \"тупо\", \"цирк\", \"страдают\", саркастическое \"поздравляю\" и т.п.

ТОНАЛЬНОСТЬ (выбери и строго придерживайся):
{tone_rules}

ОБЯЗАТЕЛЬНАЯ СИТУАЦИЯ ДЛЯ ПОСТА:
- Категория: {situation_category}
- Бриф сцены: {situation_brief}
- ТРЕБОВАНИЕ: пост должен начинаться с конкретной сцены из этого брифа (1–2 предложения), без абстракций.
- КРИТИЧНО: Если в брифе или теме упомянут конкретный способ взаимодействия (голосовой ввод, войс, бот, фото чека, тарелка), ты ОБЯЗАН вплести этот способ в текст. Не обобщай до "внес данные/отправил", пиши то, что указано в задаче (например, "отправил голосом").

АНТИ-ПОВТОРЫ:
- Запрещено использовать эти заезженные формулировки/хуки (даже перефразом): {banned_cliches}
- Запрещено начинать с «Через неделю…» и заканчивать «Разница — одно фото».

ЭМОДЗИ:
- Максимум 3 эмодзи на весь пост. Не больше.

СТРОГИЕ ФАКТЫ О FoodFlow (НЕ ВЫДУМЫВАТЬ НИЧЕГО СВЕРХ ЭТОГО):
1. FoodFlow умеет считать строку ввода (голос, текст, фото еды, фото чека) и выдавать КБЖУ (Калории, Белки, Жиры, Углеводы). 
2. МАГИЯ: ввод без ручного труда. Просто фото (тарелка/чек) или голосовое сообщение — и бот возвращает КБЖУ.

КОНТЕКСТ ВРЕМЕНИ (ДЛЯ ТЕБЯ, НЕ ДЛЯ ТЕКСТА):
- Сезон: {season}. Месяц: {current_month}. Год: {now.year}.
- Задача: Сделай 0-1 органичную отсылку к сезону/погоде/поводу (например: "весной", "сезон шашлыков", "в жару", "когда темнеет рано").
- ЗАПРЕЩЕНО: писать в тексте поста месяц/год/дату (никаких "апрель 2026", "в 2026", "сейчас {current_month}"). Сезонные маркеры — ок.

КОНКРЕТНАЯ ИНСТРУКЦИЯ К ПОСТУ (СЦЕНАРИЙ):
{scenario_instruction}

ТЕМА ДЛЯ ПРИМЕРА: "{topic}"

ПРАВИЛА НАПИСАНИЯ:
- Длина: 2-3 абзаца. Удар -> смысл -> действие.
- Вставь микро-сценарий (Сфоткал -> Отправил -> Получил КБЖУ).
- Пиши СТРОГО гендерно-нейтрально.
- ЭМОДЗИ: максимум 3 штуки на весь пост.
- ЗАПРЕЩЕНО галлюцинировать витамины. Только КБЖУ.
- ЗАПРЕЩЕНО “объяснялки/лекции”: не используй такие заходы: {lecture_phrases}
- ЗАПРЕЩЕНО ватные финалы: не заканчивай словами типа {banned_soft_endings}
- Слово «КБЖУ» используй максимум 1 раз; дальше пиши «цифры/сводка/понятно что съел(а)».

КОНЕЦ ПОСТА (ЖЕСТКИЙ КРЮЧОК):
Добавь короткую жёсткую финальную строку: 1 предложение, ДО 10 СЛОВ.
Формат: выбор/давление на действие (либо/либо или или/или).
Запрещено: “остальное…”, “бот возьмёт…”, “не переживай…”.
Примеры: "Либо видишь цифры, либо снова гадаешь.", "Или фиксируешь, или теряешь из учёта."

АНТИ-ПОВТОР ФИНАЛОВ (КРИТИЧНО):
- Запрещено повторять одну и ту же финальную формулировку более 2 раз подряд.
- Твои последние финалы (НЕ ПОВТОРЯТЬ дословно/почти дословно): {recent_final_hooks}
- Каждый финал должен звучать по-разному, но делать одно: дожимать к действию.

После текста напиши промпт на АНГЛИЙСКОМ для генератора картинок (Photorealistic cyberpunk style, food/lifestyle).

ОТВЕТ ВЕРНИ СТРОГО В ВИДЕ ЧИСТОГО JSON (БЕЗ MARKDOWN ФОРМАТИРОВАНИЯ И КОДА):
{{
  "scenario": "{scenario}",
  "text": "текст поста",
  "image_prompt": "english prompt for image"
}}
"""
    
    # Пробуем модели по очереди
    for model_name in MODELS:
        # 3 попытки на каждую модель
        for attempt in range(1, 4):
            logger.info(f"🤖 [Attempt {attempt}] Использование модели {model_name}...")
            
            payload = {
                "model": model_name,
                "messages": [{"role": "user", "content": prompt}],
                "response_format": { "type": "json_object" }
            }
            
            try:
                # ТАЙМАУТ 30 секунд (чтобы не висеть, если модель тупит)
                data = await openrouter_post(headers=headers, payload=payload, timeout=30.0)
                raw_content = data['choices'][0]['message']['content']
                
                # Очистка и парсинг
                raw_content = raw_content.strip()
                if raw_content.startswith('```json'): raw_content = raw_content[7:]
                elif raw_content.startswith('```'): raw_content = raw_content[3:]
                if raw_content.endswith('```'): raw_content = raw_content[:-3]
                
                try:
                    result = json.loads(raw_content.strip())
                    logger.info(f"✅ Успешно сгенерировано через {model_name}")
                    return result
                except json.JSONDecodeError:
                    # Ищем JSON в тексте если формат сбился
                    match = re.search(r'\{.*\}', raw_content, re.DOTALL)
                    if match:
                        result = json.loads(match.group(0))
                        return result
                    raise ValueError("JSON parse failed")
                        
            except Exception as e:
                logger.warning(f"⚠️ Ошибка {model_name} (Attempt {attempt}): {e}")
                # Если это таймаут или сетевая ошибка, ждем секунду перед ретраем
                await asyncio.sleep(1)
                continue
        
        logger.error(f"❌ Модель {model_name} провалила все попытки. Переход к следующей...")

    # Если всё упало
    return {
        "text": "Системная задержка в Кибер-Пространстве. Сегодня считаем сами, пока я чиню нейросети.", 
        "image_prompt": "error", 
        "scenario": scenario
    }


