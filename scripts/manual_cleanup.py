import json
import os

input_file = "data/march_8_ai_gifts_v2.json"

with open(input_file, "r", encoding="utf-8") as f:
    gifts = json.load(f)

for user_id, text in gifts.items():
    # Общие замены для вычистки негатива
    text = text.replace("Верни коктейль на роль помощника, а не главного героя", "Твои коктейли — это высокотехнологичный фундамент рациона")
    text = text.replace("оставь один коктейль в день, остальное — еда. Не потому что коктейли плохие, а потому что ты достойна большего", "коктейли — это твой ключ к легкому добору белка и витаминов, продолжай в том же духе")
    text = text.replace("Белок — из тарелки, не из пакета", "Белок — твоя мощная база")
    text = text.replace("70–80% белка приходило из", "дополнять белок из коктейлей такими продуктами как")
    text = text.replace("это не строгий запрет на протеин — это приглашение разнообразить рацион", "это способ сделать твой рацион еще богаче и эффективнее")
    text = text.replace("Жидкие углеводы из коктейлей работают иначе", "Углеводы в коктейлях — это быстрая энергия")
    text = text.replace("Протеиновые коктейли занимают слишком много места. Они удобны, но твёрдая еда насыщает лучше", "Твои коктейли — это идеальное решение для контроля калорий и качественного белка")
    text = text.replace("Коктейль — отличный помощник, но не основа рациона", "Herbalife — это твой надежный фундамент для достижения цели")
    text = text.replace("Один коктейль в день — максимум", "Твой выбор в пользу функционального питания — это очень круто")
    text = text.replace("не более одного в сутки. Все остальные приёмы пищи — из настоящих продуктов. Твёрдая еда лучше насыщает", "сочетай коктейли с любимыми продуктами для максимального комфорта и сытости")
    text = text.replace("Белок — из еды, не из порошка", "Белок — это твой строительный материал")
    text = text.replace("Коктейль этого не умеет", "Коктейль дает отличную базу, которую мы вместе усилим")
    text = text.replace("Из 10 самых частых продуктов в твоём меню — 5 это продукты Гербалайф. Коктейли и добавки стали основой рациона, и мы понимаем логику: удобно, предсказуемо, быстро. Но есть один важный момент.\n\n> Заменители еды — это инструмент, а не фундамент. Когда они занимают больше половины рациона, организм начинает недополучать то, что есть только в настоящей, живой еде", "Мы видим, что продукты Гербалайф стали основой твоего рациона, и это — профессиональный подход к питанию. Удобно, точно по КБЖУ и эффективно. Ты построила отличный фундамент")
    text = text.replace("Попробуй постепенно заменять коктейли Гербалайф на обычные блюда — не резко, а шаг за шагом. Цель — чтобы не менее 70% рациона составляла настоящая, цельная еда", "Твоя система питания с Гербалайф работает отлично. Просто добавь в нее чуть больше разнообразия цельных продуктов")
    
    # Специфическая правка для Татьяны (5153798702) - если она есть
    if user_id == "5153798702":
         text = text.replace("Это не значит отказаться от добавок совсем, но натуральные продукты дают то, что никакой коктейль не воспроизведёт полностью", "Твои продукты — это база, а натуральные добавки из еды сделают её ещё совершеннее")

    gifts[user_id] = text

with open(input_file, "w", encoding="utf-8") as f:
    json.dump(gifts, f, ensure_ascii=False, indent=2)

print("Manual cleanup finished successfully.")
